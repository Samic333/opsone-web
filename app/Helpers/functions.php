 */

/**
 * Redirect to URL
 */
function redirect(string $url): never {
    header("Location: $url");
    exit;
}

/**
 * Return JSON response
 */
function jsonResponse(array $data, int $status = 200): never {
    http_response_code($status);
    header('Content-Type: application/json; charset=utf-8');
    echo json_encode($data, JSON_UNESCAPED_UNICODE);
    exit;
}

/**
 * Get base URL
 */
function baseUrl(string $path = ''): string {
    $base = rtrim(config('app.url', ''), '/');
    return $base . '/' . ltrim($path, '/');
}

/**
 * Escape HTML output
 */
function e(?string $value): string {
    return htmlspecialchars($value ?? '', ENT_QUOTES, 'UTF-8');
}

/**
 * Get session flash message
 */
function flash(string $key, string $message = null): ?string {
    if ($message !== null) {
        $_SESSION['flash'][$key] = $message;
        return null;
    }
    $msg = $_SESSION['flash'][$key] ?? null;
    unset($_SESSION['flash'][$key]);
    return $msg;
}

/**
 * Check if current user has role
 */
/**
 * Require any authenticated session — redirects to login if not logged in.
 */
function requireAuth(): void {
    if (empty($_SESSION['user'])) {
        redirect('/login');
    }
}

function hasRole(string $role): bool {
    $user = $_SESSION['user'] ?? null;
    if (!$user) return false;
    $roles = $_SESSION['user_roles'] ?? [];
    return in_array($role, $roles);
}

/**
 * Check if any of the given roles match
 */
function hasAnyRole(array $roles): bool {
    foreach ($roles as $role) {
        if (hasRole($role)) return true;
    }
    return false;
}

/**
 * Get current authenticated user
 */
function currentUser(): ?array {
    return $_SESSION['user'] ?? null;
}

/**
 * Get current tenant ID
 */
function currentTenantId(): ?int {
    if (isSingleTenant()) {
        return getFixedTenantId();
    }
    return $_SESSION['tenant_id'] ?? null;
}

/**
 * Generate CSRF token
 */
function csrfToken(): string {
    if (empty($_SESSION['csrf_token'])) {
        $_SESSION['csrf_token'] = bin2hex(random_bytes(32));
    }
    return $_SESSION['csrf_token'];
}

/**
 * Output hidden CSRF input field
 */
function csrfField(): string {
    return '<input type="hidden" name="_csrf_token" value="' . csrfToken() . '">';
}

/**
 * Verify CSRF token
 */
function verifyCsrf(): bool {
    $token = $_POST['_csrf_token'] ?? $_SERVER['HTTP_X_CSRF_TOKEN'] ?? '';
    return hash_equals(csrfToken(), $token);
}

/**
 * Generate a random API token
 */
function generateApiToken(): string {
    return bin2hex(random_bytes(32));
}

/**
 * Format datetime for display
 */
function formatDate(?string $date, string $format = 'M j, Y'): string {
    if (!$date) return '—';
    return date($format, strtotime($date));
}

/**
 * Format datetime with time
 */
function formatDateTime(?string $date): string {
    if (!$date) return '—';
    return date('M j, Y H:i', strtotime($date));
}

/**
 * Human-readable byte size (1024 base): 1.4 KB, 3.2 MB, etc.
 */
function formatBytes(?int $bytes, int $precision = 1): string {
    $bytes = (int)($bytes ?? 0);
    if ($bytes <= 0) return '—';
    $units = ['B', 'KB', 'MB', 'GB', 'TB'];
    $i     = min((int) floor(log($bytes, 1024)), count($units) - 1);
    return round($bytes / (1024 ** $i), $precision) . ' ' . $units[$i];
}

/**
 * Render status badge HTML
 */
function statusBadge(string $status): string {
    // Cockpit-light status mapping. Each status falls into exactly one of:
    //   cleared (green) | advisory (amber) | critical (red) | neutral (slate) | info (blue)
    // Spec: docs/opsone-brand-direction.md §3.
    static $cleared  = ['active','approved','published','completed','done','passed','online',
                        'acknowledged','signed','paid','open'];
    static $advisory = ['pending','in_review','review','awaiting','submitted','draft_review',
                        'requires_info','expiring','soon_due'];
    static $critical = ['suspended','rejected','revoked','expired','failed','blocked',
                        'aog','overdue','denied'];
    static $neutral  = ['inactive','draft','archived','closed','withdrawn','cancelled','offline'];

    $key = strtolower($status);
    if (in_array($key, $cleared,  true))  { $color = '#10b981'; } // --status-cleared
    elseif (in_array($key, $advisory, true)) { $color = '#f59e0b'; } // --status-advisory
    elseif (in_array($key, $critical, true)) { $color = '#ef4444'; } // --status-critical
    elseif (in_array($key, $neutral,  true)) { $color = '#7484a8'; } // muted slate
    else                                     { $color = '#3b82f6'; } // --status-info (default for unknown)

    $label = ucfirst(str_replace('_', ' ', $status));
    return "<span class=\"status-badge\" style=\"--badge-color: $color\">$label</span>";
}

/**
 * Log application event
 */
function appLog(string $message, string $level = 'info'): void {
    $logFile = __DIR__ . '/../../storage/logs/app.log';
