    <p>Go to <strong>People Ops → Training Dashboard</strong>.</p>
    <ul>
        <li>Define <strong>Training Types</strong> with a validity in months — expiry dates are then computed automatically when you record a completion.</li>
        <li>Record training per user with completion + expiry + provider + result (pass/fail/in progress/scheduled).</li>
        <li>The dashboard surfaces records expiring within 14 days past / 60 days ahead.</li>
        <li>Crew see their own records at <a href="/my-training">My Training</a>, colour-coded by days-to-expiry.</li>
    </ul>
