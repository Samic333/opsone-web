<div class="card" style="max-width:820px;">
    <h3 style="margin-top:0;">Document controller</h3>
    <p>Go to <strong>Content → Documents</strong>.</p>
    <ul>
        <li><strong>+ Upload Document</strong> — pick category, version, effective date, and <em>Audience</em> (role / department / base). Leave all audience rows empty to target all staff.</li>
        <li>Toggle <strong>Requires acknowledgement</strong> for items where you need proof of receipt.</li>
        <li>Publish replaces: use the <em>New Version</em> button on the row to upload a revision that archives the previous one.</li>
        <li>Each document has an <strong>Acknowledgement Report</strong> showing who has read/acknowledged it.</li>
        <li>All publish + ack activity is recorded in the audit log.</li>
    </ul>
</div>
