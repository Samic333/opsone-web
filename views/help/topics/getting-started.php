    <h3 style="margin-top:0;">Getting started with OpsOne</h3>
    <p>OpsOne is your airline's operational control hub. Your view is tailored to your role — everyone sees a different dashboard.</p>

    <h4>Your dashboard</h4>
    <ul>
        <li>Click <strong>Dashboard</strong> in the sidebar any time.</li>
        <li>Flash cards at the top summarise what needs your attention today.</li>
    </ul>

    <h4>Key daily tasks</h4>
    <ul>
        <li><strong>Crew:</strong> Check <em>My Roster</em>, <em>My Flights</em>, <em>My Documents</em>, <em>My Logbook</em>. Report for duty from <em>Duty Reporting</em>.</li>
        <li><strong>Scheduler:</strong> Use <em>Roster Workbench</em>, <em>Flights</em>, and <em>Coverage</em>.</li>
        <li><strong>HR / admin:</strong> Visit <em>HR Workflow</em>, <em>Users</em>, <em>Compliance</em>.</li>
        <li><strong>Safety team:</strong> Start at <em>Safety Dashboard</em>.</li>
    </ul>

    <h4>Notifications</h4>
    <p>The <strong>🔔 bell</strong> in the header shows unread notifications (yellow = loud / important). Click it any time to open the notification inbox.</p>

    <h4>Need more?</h4>
    <p>Return to <a href="/help">the help hub</a> for guides specific to your role.</p>
