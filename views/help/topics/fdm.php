    <h3 style="margin-top:0;">FDM uploads &amp; events</h3>
    <p><strong>Safety → FDM Data</strong> is your working view.</p>
    <ul>
        <li>Upload FDM CSV files. The upload appears in the list with parsed flight metadata.</li>
        <li>Add events (hard landing, speed exceedance, etc.) to each upload. Events can be <strong>tagged to a pilot</strong> — if the flight is already in the flight board, the captain is auto-matched.</li>
        <li>Tagged pilots receive an in-app notification and see the event under <a href="/my-fdm">My FDM Events</a>, where they can acknowledge.</li>
    </ul>
